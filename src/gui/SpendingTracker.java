    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            java.util.Date datee = dateofspending.getDate();
            String amountt = amountofspending.getText();
            String category = (String) show_categoey.getSelectedItem();
            if (datee != null && !amountt.equals("") && !category.equals("")) {
                int amount = Integer.parseInt(amountt);
                java.sql.Date date = new java.sql.Date(datee.getTime());
                db.DbConnect.s.executeUpdate("insert into spendings(category ,s_date , amount) "
                        + "values('" + category + "','" + date + "'," + amount + ")");
                JOptionPane.showMessageDialog(null, "Expense Added Successfuly!!");
                getEntries();

            } else {
                JOptionPane.showMessageDialog(null, "Plz fill all details!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = table.getSelectedRow();

        if (selectedRow != -1) {
            int r = JOptionPane.showConfirmDialog(null, "Do you really wanna delete", "Deletion Conformation", JOptionPane.YES_NO_OPTION);
            if (r == JOptionPane.YES_OPTION) {
                //        selected value get
                try {

                    int id = (int) table.getValueAt(selectedRow, 0);
                    db.DbConnect.s.executeUpdate("delete from spendings where sid=" + id + "");
                    JOptionPane.showMessageDialog(null, "Successfully Delete!");
                    getEntries();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);

                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

