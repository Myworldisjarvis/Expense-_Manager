    private void getEntries() {
        try {

//        get table modal
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) table.getModel();
            //    for empty karne ke liye
            int rc = dtm.getRowCount();
            while (rc-- != 0) {
                dtm.removeRow(0);
            }

            java.time.LocalDate cd = java.time.LocalDate.now();
            java.time.LocalDate bd = cd.minusDays(20);
            ResultSet rs = db.DbConnect.s.executeQuery("select * from spendings where s_date<= '" + cd + "' and s_date>='" + bd + "' ");

//     show date in table or modal
            int total = 0;
            while (rs.next()) {
                int t = rs.getInt("amount");
                total += t;
                Object o[] = {
                    rs.getInt("sid"),
                    rs.getString("category"),
                    rs.getDate("s_date"), t
                };
                dtm.addRow(o);
            }
            totalvalue.setText(total + "");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

